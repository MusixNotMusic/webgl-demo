/**
 * 随机分配
 * @param start
 * @param dest
 * @param size
 * @returns {[]}
 */
export function generateRadarPoint(map, start, dest, size) {
    const exaggeration = map.getTerrain().exaggeration;
    radarTestSet.forEach(item => item.alt = item.alt1 * exaggeration);
    return radarTestSet;

    const Station = [];
    const applyX = (t) => start[0] * (1 - t) + dest[0] * t;
    const applyY = (t) => start[1] * (1 - t) + dest[1] * t;

    let seed = 0;
    for (let i = 0; i < size; i++) {
        seed = 1 / size * i;
        const sign = i % 2 === 0 ? 1 : -1;

        const dx = 0.1 * Math.random();
        const dy = 0.5 * Math.random();

        const x = applyX(seed) + sign * dx;
        const y = applyY(seed) + sign * dy;
        Station.push({
            lon: x,
            lat: y,
            alt: map.queryTerrainElevation({ lng: x, lat: y }, { exaggerated: true }),
            height: 1000,
            name: `radar-${i + 1}`,
            radius: 75 // km
        });
    }

    return Station;
}